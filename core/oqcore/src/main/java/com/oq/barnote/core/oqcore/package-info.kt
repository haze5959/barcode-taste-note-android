package com.oq.barnote.core.oqcore

/**
 * OQCore 유틸리티 모듈
 *
 * iOS OQCore에 대응하는 프로젝트 공용 유틸리티 라이브러리입니다.
 * Android 프레임워크에 종속적이지 않은 순수 로직과,
 * Compose 기반 공용 UI 컴포넌트를 포함합니다.
 *
 * 패키지 구조:
 * - cache: 캐시 관리 (CacheManager, CacheItem)
 * - extension: Kotlin 확장 함수 모음 (Array, String, Date 등)
 * - log: 로깅 유틸리티 (OQLog)
 * - file: 파일 관리 유틸리티 (OQFileManager)
 * - haptic: 햅틱 피드백 서비스
 * - keychain: 보안 저장소 (KeyStore wrapper)
 * - model: 공용 모델 (Palette, Report, CacheItem 등)
 * - network: 네트워크 클라이언트 유틸리티
 * - share: SNS 공유 유틸리티
 * - type: 공용 타입 정의
 * - util: 기타 범용 유틸리티
 * - ui.component: 공용 Compose UI 컴포넌트 (Alert, Toast, Skeleton 등)
 * - ui.modifier: 공용 Compose Modifier (ButtonStyle, PopupModifier 등)
 * - ui.theme: 테마 전환 관리
 */
